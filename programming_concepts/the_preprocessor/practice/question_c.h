#ifndef QUESTION_C_H
#define QUESTION_C_H

#define PI 3.14

// circle
#define AREAOFCIRCLE(r) (PI * r * r)
#define PERIMETEROFCIRCLE(r) (2 * PI * r)

// triangle
#define AREAOFTRIANGLE(b, h) (0.5 * b * h)
#define PERIMETEROFTRIANGLE(a, b, c) (a + b + c)

// square
#define AREAOFSQUARE(a) (a * a)
#define PERIMETEROFSQUARE(a) (4 * a)

#endif
